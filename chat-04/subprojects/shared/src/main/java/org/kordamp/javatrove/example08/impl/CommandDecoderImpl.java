package org.kordamp.javatrove.example08.impl;

import com.fasterxml.jackson.databind.ObjectMapper;
import io.netty.buffer.ByteBuf;
import io.netty.channel.ChannelHandlerContext;
import io.netty.handler.codec.ByteToMessageDecoder;
import org.kordamp.javatrove.example08.Command;
import org.kordamp.javatrove.example08.CommandDecoder;

import javax.inject.Inject;
import java.util.List;

/**
 * @author Andres Almiray
 */
public class CommandDecoderImpl extends ByteToMessageDecoder implements CommandDecoder {
    @Inject private ObjectMapper objectMapper;

    @Override
    protected void decode(ChannelHandlerContext ctx, ByteBuf in, List<Object> out) throws Exception {
        byte[] bytes = new byte[in.readableBytes()];
        in.readBytes(bytes);
        out.add(objectMapper.readValue(bytes, Command.class));
    }
}
